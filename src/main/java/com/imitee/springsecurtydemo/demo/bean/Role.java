package com.imitee.springsecurtydemo.demo.bean;

/**
 * @author 罗文俊
 * 2021/2/20
 */
public class Role {
    private int id;
    private String username;
    private String name;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
